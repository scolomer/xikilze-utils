package org.xikilze.utils.csv;

public class CSVException extends Exception {

	private static final long serialVersionUID = 7779987016116756347L;

	public CSVException() {
		super();
	}

	public CSVException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CSVException(String arg0) {
		super(arg0);
	}

	public CSVException(Throwable arg0) {
		super(arg0);
	}

}
